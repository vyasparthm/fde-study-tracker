DAY_NAMES = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday",
             "Saturday", "Sunday"]

# Our week layout starting from Friday:
# index 0=Fri, 1=Sat, 2=Sun, 3=Mon, 4=Tue, 5=Wed, 6=Thu
WEEK_DAY_ORDER = [4, 5, 6, 0, 1, 2, 3]  # Fri=4, Sat=5, Sun=6, Mon=0 ...


def generate_schedule() -> List[Dict]:
    """Generate the full day-by-day schedule with actual dates."""
    schedule = []
    for week_plan in WEEKLY_PLANS:
        week_num = week_plan["week"]
        week_start = START_DATE + timedelta(weeks=week_num - 1)
        for i, day_plan in enumerate(week_plan["days"]):
            day_date = week_start + timedelta(days=i)
            weekday = day_date.weekday()  # 0=Mon ... 6=Sun
            is_weekend = weekday >= 5  # Sat=5, Sun=6
            hours = 7 if is_weekend else 3.25
            schedule.append({
                "date": day_date,
                "date_str": day_date.strftime("%Y-%m-%d"),
                "day_name": DAY_NAMES[weekday],
                "week": week_num,
                "phase": week_plan["phase"],
                "theme": week_plan["theme"],
                "topic": day_plan["topic"],
                "tasks": day_plan["tasks"],
                "resources": day_plan.get("resources", []),
                "target_hours": hours,
                "is_weekend": is_weekend,
            })
    return schedule


